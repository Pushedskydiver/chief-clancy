/**
 * E2E: Lifecycle contract tests — data flow between brief → plan → implement.
 *
 * Validates that the data contracts between packages hold with realistic
 * fixture content. Uses real filesystem operations and the actual parsers
 * (not mocks). No Claude invocation — these are structural contract tests.
 *
 * Always runs in CI (no hasCredentials gate).
 */
import { createHash } from 'node:crypto';
import { mkdtempSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { join } from 'node:path';

import { afterEach, describe, expect, it } from 'vitest';

import {
  checkApprovalStatus,
  parsePlanFile,
  toSyntheticTicket,
} from '@chief-clancy/dev';

import {
  DEFAULT_PLAN,
  writeApprovalMarker,
  writePlanFile,
} from '../helpers/local-plan-setup.js';

// ─── Temp dir management ────────────────────────────────────────────────────

let tempDir: string | undefined;

function createTempDir(): string {
  tempDir = mkdtempSync(join(tmpdir(), 'clancy-contract-'));
  return tempDir;
}

afterEach(() => {
  if (tempDir) {
    rmSync(tempDir, { recursive: true, force: true });
    tempDir = undefined;
  }
});

